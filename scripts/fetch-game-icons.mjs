// Downloads semantically-matched icons from game-icons.net (via the
// game-icons/icons GitHub raw endpoint) into public/creatures/*.svg,
// overwriting the letter-in-circle placeholders for any catalog id that
// appears in ICON_MAPPING. Entries not in the mapping are left alone.
//
//   node scripts/fetch-game-icons.mjs
//
// The script is idempotent: re-running replaces each mapped file with a
// fresh copy, and regenerates CREDITS.md from the mapping. SVG content
// is written verbatim — do NOT modify the source.
//
// Licensing: all game-icons.net icons are Creative Commons BY 3.0. See
// CREDITS.md (generated by this script) for per-icon attribution.
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import { ICON_MAPPING, AUTHOR_DISPLAY } from './icon-mapping.mjs';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = path.join(__dirname, '..');
const OUT_DIR   = path.join(REPO_ROOT, 'public', 'creatures');
const CREDITS   = path.join(REPO_ROOT, 'CREDITS.md');
const BASE      = 'https://raw.githubusercontent.com/game-icons/icons/master';

fs.mkdirSync(OUT_DIR, { recursive: true });

function humanize(name) {
  return name
    .split('-')
    .map(w => w.charAt(0).toUpperCase() + w.slice(1))
    .join(' ');
}

function authorDisplay(a) {
  return AUTHOR_DISPLAY[a] || a;
}

const downloaded = [];
const skipped = [];

for (const [id, { author, name }] of Object.entries(ICON_MAPPING)) {
  const url = `${BASE}/${author}/${name}.svg`;
  try {
    const res = await fetch(url);
    if (!res.ok) { skipped.push({ id, url, reason: `HTTP ${res.status}` }); continue; }
    const body = await res.text();
    if (!body.includes('<svg')) { skipped.push({ id, url, reason: 'not svg' }); continue; }
    const outPath = path.join(OUT_DIR, `${id}.svg`);
    fs.writeFileSync(outPath, body);
    downloaded.push({ id, author, name, url });
    console.log(`downloaded  ${id.padEnd(22)} ${author}/${name}`);
  } catch (e) {
    skipped.push({ id, url, reason: e.message });
  }
}

// Sort alphabetically by id for a stable CREDITS.md. Idempotent by
// construction — the same mapping produces the same file every time.
downloaded.sort((a, b) => a.id.localeCompare(b.id));

const lines = [
  '# Credits',
  '',
  '## Icons',
  '',
  'Icons under `public/creatures/` are from [game-icons.net](https://game-icons.net/) by various contributors, licensed under [CC BY 3.0](https://creativecommons.org/licenses/by/3.0/). Unmodified from originals.',
  '',
];
for (const { id, author, name } of downloaded) {
  const title = humanize(name);
  const who = authorDisplay(author);
  const pageUrl = `https://game-icons.net/1x1/${author}/${name}.html`;
  lines.push(`- \`${id}.svg\` — "${title}" by ${who} — ${pageUrl}`);
}
lines.push('');
fs.writeFileSync(CREDITS, lines.join('\n'));

console.log('');
console.log(`downloaded: ${downloaded.length}`);
console.log(`skipped:    ${skipped.length}`);
if (skipped.length) {
  for (const s of skipped) console.log(`  ${s.id.padEnd(22)} ${s.reason}  ${s.url}`);
}
console.log(`CREDITS.md: ${CREDITS}`);
